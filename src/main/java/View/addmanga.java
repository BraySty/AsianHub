    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int option = fileChooser.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            String imgPath = file.getAbsolutePath();
            try {
                if (!imgPath.isBlank()) {
                    if (file.exists() && file.isFile() && (file.getName().endsWith("png")) || file.getName().endsWith("jpg") || file.getName().endsWith("jpeg") || file.getName().endsWith(".gif")) {
                        Imagen imagen = imagen = new Imagen(file);
                        jLabelImagen.setIcon(new ImageIcon(imagen.loadImageByPixelHeightSize(imageHeight)));
                    }
                }
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        } else if (option == JFileChooser.CANCEL_OPTION) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

